    public void continuar_proceso(int num) {
        if (num == 0) {
            p.cerrar(0);
            System.out.println("Deteniendo hilos");
        } else if (num == 1) {
            p.abrir(0);
        }
    }

    public void iniciar_conexion() throws IOException {
        this.serv = new ServerSocket(5000, 100);
        boolean hay_conexion = true;
        while (hay_conexion) {
            sock = serv.accept();
            Thread hilo = new Thread(){
                public void run(){
                    try {
                        gestionar_conexion(sock);
                    } catch (IOException ex) { }
                }
            };
            pool_hilos.execute(hilo);
        }
    }
    
    public void gestionar_conexion(Socket sock) throws IOException{
        entrada = new DataInputStream(sock.getInputStream());
        salida = new DataOutputStream(sock.getOutputStream());
        
        salida.writeUTF("Hola");
        
        boolean hay_conexion = true;
        while(hay_conexion){
            String msg = entrada.readUTF();
            
            if("Detener".equals(msg)){
               continuar_proceso(0); 
            }else if("Reanudar".equals(msg)){
                continuar_proceso(1);
            }else{
                System.out.println("Comando incorrecto");
            }
        }
        
    }
